function initPreloader() {
    const preloader = document.getElementById('preloader');
    if (!preloader) return;

    window.addEventListener('load', () => {
        setTimeout(() => {
            preloader.classList.add('opacity-0', 'pointer-events-none', 'transition-opacity', 'duration-500');
            setTimeout(() => {
                preloader.style.display = 'none';
            }, 500);
        }, 450);
    });
}

function initMenu() {
    const button = document.getElementById('menuButton');
    const mobileMenu = document.getElementById('mobileMenu');
    const mobileLinks = document.querySelectorAll('.mobile-link');
    if (!button || !mobileMenu) return;

    button.addEventListener('click', () => {
        mobileMenu.classList.toggle('hidden');
    });

    mobileLinks.forEach((link) => {
        link.addEventListener('click', () => {
            mobileMenu.classList.add('hidden');
        });
    });
}

function initLanguageToggle() {
    const savedLanguage = localStorage.getItem('preferredLanguage');
    const language = savedLanguage && translations[savedLanguage] ? savedLanguage : 'id';

    document.querySelectorAll('[data-lang-toggle]').forEach((button) => {
        button.addEventListener('click', () => {
            const nextLanguage = button.getAttribute('data-lang-toggle');
            setLanguage(nextLanguage);
        });
    });

    setLanguage(language);
}

function setLanguage(language) {
    if (!translations[language]) return;

    currentLanguage = language;
    localStorage.setItem('preferredLanguage', language);
    document.documentElement.lang = language === 'id' ? 'id' : 'en';
    document.title = translations[language].page_title;

    document.querySelectorAll('[data-i18n]').forEach((element) => {
        const key = element.getAttribute('data-i18n');
        if (translations[language][key]) {
            element.textContent = translations[language][key];
        }
    });

    document.querySelectorAll('[data-i18n-html]').forEach((element) => {
        const key = element.getAttribute('data-i18n-html');
        if (translations[language][key]) {
            element.innerHTML = translations[language][key];
        }
    });

    const heroBadge = document.querySelector('[data-i18n="hero_badge"]');
    if (heroBadge) {
        heroBadge.innerHTML = translations[language].hero_badge;
    }

    document.querySelectorAll('[data-lang-toggle]').forEach((button) => {
        button.classList.toggle('active', button.getAttribute('data-lang-toggle') === language);
    });

    startStackRotator(language);
    initYear();
}

function startStackRotator(language) {
    const element = document.getElementById('stackRotator');
    if (!element) return;

    const stacks = stackRotatorValues[language] || stackRotatorValues.id;
    let index = 0;
    element.textContent = stacks[0];

    if (stackInterval) {
        clearInterval(stackInterval);
    }

    stackInterval = setInterval(() => {
        index = (index + 1) % stacks.length;
        element.classList.add('opacity-0', 'translate-y-1');
        setTimeout(() => {
            element.textContent = stacks[index];
            element.classList.remove('opacity-0', 'translate-y-1');
        }, 180);
    }, 1800);
}

function initScrollProgress() {
    const progress = document.getElementById('scrollProgress');
    if (!progress) return;

    window.addEventListener('scroll', () => {
        const totalHeight = document.documentElement.scrollHeight - window.innerHeight;
        const percentage = totalHeight > 0 ? (window.scrollY / totalHeight) * 100 : 0;
        progress.style.width = `${percentage}%`;
    });
}

function initReveal() {
    const elements = document.querySelectorAll('.reveal');
    if (!elements.length) return;

    const observer = new IntersectionObserver((entries) => {
        entries.forEach((entry) => {
            if (entry.isIntersecting) {
                entry.target.classList.add('is-visible');
                observer.unobserve(entry.target);
            }
        });
    }, { threshold: 0.14 });

    elements.forEach((element, index) => {
        element.style.transitionDelay = `${Math.min(index * 40, 240)}ms`;
        observer.observe(element);
    });
}

function initActiveLinks() {
    const sections = document.querySelectorAll('main section[id]');
    const links = document.querySelectorAll('.nav-link, .mobile-link');
    const navbar = document.getElementById('navbar');

    const updateActiveLink = () => {
        let activeId = 'home';

        sections.forEach((section) => {
            const top = section.offsetTop - 140;
            const bottom = top + section.offsetHeight;
            if (window.scrollY >= top && window.scrollY < bottom) {
                activeId = section.id;
            }
        });

        links.forEach((link) => {
            const target = link.getAttribute('href')?.replace('#', '');
            link.classList.toggle('active', target === activeId);
        });

        if (navbar) {
            navbar.classList.toggle('border-cyanaccent/20', window.scrollY > 20);
        }
    };

    updateActiveLink();
    window.addEventListener('scroll', updateActiveLink);
}

function initBackToTop() {
    const button = document.getElementById('backToTop');
    if (!button) return;

    window.addEventListener('scroll', () => {
        button.classList.toggle('hidden', window.scrollY < 480);
        button.classList.toggle('flex', window.scrollY >= 480);
    });
}

function initYear() {
    const year = document.getElementById('currentYear');
    if (year) {
        year.textContent = new Date().getFullYear();
    }
}
